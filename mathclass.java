public class mathclass {
 public static void main(String[] args) {
     System.out.println("minimum number is:"+ Math.min(65,65.5));
     System.out.println("maximum number is:"+ Math.max(100,100.1));
     System.out.println("square root of number is:"+ Math.sqrt(144));
     System.out.println("Absolute number is:"+ Math.abs(-50));
     System.out.println("power of x and y is:"+ Math.pow(4,2));
 }   
}
